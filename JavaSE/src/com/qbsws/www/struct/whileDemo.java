package com.qbsws.www.struct;

public class whileDemo {
    public static void main(String[] args) {
        int a = 5;
        while (a<5){
            System.out.println(a);
            a++;
        }
        System.out.println("====================");
        int b=5;
        do {
            System.out.println(b);
            b++;
        }while(b<5);
    }
}
